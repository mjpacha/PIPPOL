package modelo;

/**
 *
 * @author WilliamMiguel
 */
public class regalos {    
    int idReg;
    int idPub;
    int idUser;

    public int getIdReg() {
        return idReg;
    }

    public void setIdReg(int idReg) {
        this.idReg = idReg;
    }

    public int getIdPub() {
        return idPub;
    }

    public void setIdPub(int idPub) {
        this.idPub = idPub;
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    @Override
    public String toString() {
        return "regalos{" + "idReg=" + idReg + ", idPub=" + idPub + ", idUser=" + idUser + '}';
    }

    
    
}
